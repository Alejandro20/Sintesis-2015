              <div class="info">
              
<p>
Subscriure's al butlletí de SportSites és la forma més còmode i senzilla de mantenir-se puntualment informat sobre les nostres activitats i accedir a totes aquelles notícies i articles que poden ser del seu interès.
<br><br>




Abans de poder fer-ho, però, és imprescindible que es registri com a usuari del web, si és que encara no disposa d'un compte. 
Com a usuari registrat, vostè podrà comentar els nostres llocs, publicats a la nostra pàgina i les entrades dels diferents llocs, participar als llocs o expressar la seva opinió responent a com son els diferents espais. Tot això a banda de subscriure's, si així ho desitja, et pots registrar.
<br><br>




Per suposat, vostè serà lliure en tot moment per donar-se de baixa de la nostra llista de distribució i deixar de rebre el butlletí. Si està interessat a cancel·lar la seva subscripció, només cal que ens enviï un correu electrònic a l'adreça info@sportsites.com especificant que no vol continua rebent el newsletter o anar al seu perfil de usuari i deshabilitar la opcio de subscripcio que te a la seva compte.
